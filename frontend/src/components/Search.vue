      <!-- Close button visible when search is active -->
      <button
        v-if="active"
        class="action"
        @click="close"
        :aria-label="$t('buttons.close')"
        :title="$t('buttons.close')"
      >
        <i class="material-icons">close</i>
      </button>
      <!-- Search icon when search is not active -->
      <i v-else class="material-icons">search</i>
      <!-- Input field for search -->
      <input
        id="main-input"
        class="main-input"
        type="text"
        @keyup.exact="keyup"
        @input="submit"
        ref="input"
        :autofocus="active"
        v-model.trim="value"
        :aria-label="$t('search.search')"
        :placeholder="$t('search.search')"
      />
    </div>

    <!-- Search results for desktop -->
    <div v-show="active" id="results" ref="result">
      <div class="inputWrapper" style="display: flex">
        <select
          v-if="multipleSources"
          class="searchContext input"
          aria-label="search-path"
          v-model="selectedSource"
          :value="selectedSource"
          @change="updateSource"
        >
          <option v-for="(info, name) in sourceInfo" :key="info" :value="name">
            {{ name }}
          </option>
        </select>

        <!-- Formatted display of selected value -->
        <div class="searchContext">{{ contextText }}</div>
      </div>

      <div id="result-list">
        <div>
          <div v-if="active">
            <div v-if="isMobile">
              <ButtonGroup
                :buttons="toggleOptionButton"
                @button-clicked="enableOptions"
                @remove-button-clicked="disableOptions"
              />
            </div>
            <div v-show="showOptions">
              <!-- Button groups for filtering search results -->
              <ButtonGroup
                :buttons="folderSelect"
                @button-clicked="addToTypes"
                @remove-button-clicked="removeFromTypes"
                @disableAll="folderSelectClicked()"
                @enableAll="resetButtonGroups()"
              />
              <ButtonGroup
                :buttons="typeSelect"
                @button-clicked="addToTypes"
                @remove-button-clicked="removeFromTypes"
                :isDisabled="isTypeSelectDisabled"
              />
              <!-- Inputs for filtering by file size -->
              <div class="sizeConstraints">
                <div class="sizeInputWrapper">
                  <p>Smaller Than:</p>
                  <input
                    class="sizeInput"
                    v-model="smallerThan"
                    type="number"
                    min="0"
                    placeholder="number"
                  />
                  <p>MB</p>
                </div>
                <div class="sizeInputWrapper">
                  <p>Larger Than:</p>
                  <input
                    class="sizeInput"
                    v-model="largerThan"
                    type="number"
                    placeholder="number"
                  />
                  <p>MB</p>
                </div>
              </div>
            </div>
          </div>
        </div>
        <!-- Loading icon when search is ongoing -->
        <p v-show="isEmpty && isRunning" id="renew">
          <i class="material-icons spin">autorenew</i>
        </p>
        <!-- Message when no results are found -->
        <div class="searchPrompt" v-show="isEmpty && !isRunning">
          <p>{{ noneMessage }}</p>
          <div class="helpButton" @click="toggleHelp()">Help</div>
        </div>
        <!-- Help text section -->
        <div class="helpText" v-if="showHelp">
          <p>
            Search occurs on each character you type (3 character minimum for search
            terms).
          </p>
          <p>
            <b>The index:</b> Search utilizes the index which automatically gets updated
            on the configured interval (default: 5 minutes). Searching when the program
